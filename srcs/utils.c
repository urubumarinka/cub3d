#include "../inc/cub3d.h"

int	empty_line(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while(s[i])
	{
		if (s[i] != '\0' && s[i] != '\n' && s[i] != '\t' &&
				s[i] != ' ' && s[i] != '\v' && s[i] != '\r')
			return (0);
		i++;
	}
	return (1);
}
